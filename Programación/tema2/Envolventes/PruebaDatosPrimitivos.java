package Envolventes;

public class PruebaDatosPrimitivos {
	public static void main(String[]args) {
		
		
		System.out.println("Maximo y minimo valor para un dato byte: ");
		System.out.println(Byte.MIN_VALUE+ " "+ Byte.MAX_VALUE);
		System.out.println(" ");
		
		System.out.println("Maximo y minimo valor para un tipo de dato short:");
		System.out.println(Short.MIN_VALUE+" "+Short.MAX_VALUE);
		System.out.println(" ");
		
		System.out.println("Maximo y minimo valor para un tipo de dato int:");
		System.out.println(Integer.MIN_VALUE+" "+Integer.MAX_VALUE);
		System.out.println(" ");
		
		System.out.println("Maximo y minimo valor para un tipo de dato long");
		System.out.println(Long.MIN_VALUE+" "+ Long.MAX_VALUE);
		System.out.println(" ");
		
		System.out.println("Maximo y minimo valor para un tipo de dato float");
		System.out.println(Double.MIN_VALUE+" "+Double.MAX_VALUE);
		System.out.println(" ");
		
	
		
	}

}
