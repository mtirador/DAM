package pkt;

public class EjercicioVariable2 {
	public static void main(String args[]) {
		
		int x=5;
		int y=2;
		double n=13.5;
		double m=5.3;
		
		//operaciones aritmeticas con las variables
		
		System.out.println("x+y= " + (x+y)); 
		System.out.println("x-y= "+ (x-y));
		System.out.println("x*y= "+ (x*y));
		System.out.println("x/y= " +(x/y));
		System.out.println("x%y= "+ (x%y));
		
		System.out.println("n+m= "+ (n+m));
		System.out.println("n-m= " +(n-m));
		System.out.println("n*m= " +(n*m));
		System.out.println("n/m= "+ (n/m));
		System.out.println("n%m= "+ (n%m));
		
		System.out.println("x+n= "+ (x+n));
		System.out.println("y/m= "+ (y/m));
		System.out.println("y%m= "+ (y%m));
		
		//ahora el doble de cada variable
		
		System.out.println("2x= " + (x*2));
		System.out.println("2y= " + (y*2));
		System.out.println("2n= " + (n*2));
		System.out.println("2m= " + (m*2));
	}

}
